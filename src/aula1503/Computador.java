package aula1503;

public class Computador {
	private String marca;
	private String cor;
	private String modelo;
	private String serie;
	private double valor;

	public Computador(String marca, String cor, String modelo, String serie, double valor) {
		this.marca = marca;
		this.cor = cor;
		this.modelo = modelo;
		this.serie = serie;
		this.valor = valor;
	}
}
